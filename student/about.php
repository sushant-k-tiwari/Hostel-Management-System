Engineering (ECE) and PhD in CSE, ECE and Humanities and Basic Sciences from its city campus located at Mantripukhri, Imphal. 
Although a new institute, the campus has adequate classrooms, laboratories for CSE and ECE, conference hall, playground, hostels, faculty quarters, 
guest house and own administrative offices. The excellent student-faculty ratio enables 360-degree technical supervision and personal development of each student.

					
IIITSM is one of the 20 IIITs set up by the Government of India in PPP mode. 
Being an <a style="text-decoration:none;" href="http://iiitmanipur.ac.in/snippets/IIIT-PPP-Act-2017-dtd-09-Aug-2017.pdf" target="_blank">Institute of National Importance</a>

